import { ExpansionPanel, ExpansionPanels, Button } from 'sveltify/src';

let value = [2];

function toggle(n) {
  if (value.includes(n)) {
    value.splice(value.indexOf(n), 1);
  } else {
    value.push(n);
  }
  value = value;
}

</script>

